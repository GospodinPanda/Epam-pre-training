package by.epam.preTraining.harshunou.exceptions;

public class CustomTreeProjectException extends Exception {
    public CustomTreeProjectException() {
        super();
    }

    public CustomTreeProjectException(String message) {
        super(message);
    }

    public CustomTreeProjectException(String message, Throwable cause) {
        super(message, cause);
    }

    public CustomTreeProjectException(Throwable cause) {
        super(cause);
    }
}
