package by.harshunou.firsttask.first;

public class HeavyDinosaur {

    public static double calcInKilo(int a) {
        double kilo;
        kilo = a / 1000.0;
        return kilo;
    }

    public static double calcInTons(int a) {
        double tons;
        tons = a / 1000000.0;
        return tons;
    }
}
